# BASIC TA heavy MACD + RSI trend trading strategy
# Look for signals with macd and confirm with RSI. Finish transaction once you
# receive second opposite signal. Does not work in sideways markets, only in trending
# markets

# BUY:
# Prepare to BUY when macd difference line crosses above the signal line
# Confirm a BUY when RSI is about to approachs ouverbought upwards to 70 RSI or RSI rises upwards
# and leaves oversold at 30

# SHORT:
# Prepare to SHORT when macd difference line crosses below signal line 
# Confirm a SHORT when RSI declines downwards and leaves overbought zone at 70 or SHORT when
# RSI approaches downwards into oversold at 30

# Do not execute trades when RSI between 45-55
